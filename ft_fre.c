#include "pipex.h"

void	ft_fre(char **cmd_t,	char *cmd)
{
	int	i;

	i = 0;
	free(cmd);
	while (cmd_t[i])
	{
		free(cmd_t[i]);
		i++;
	}
	free(cmd_t);
}
